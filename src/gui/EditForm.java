    private void imgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imgBtnActionPerformed

        imageChooser.setDialogTitle("Choose an Image!");
        imageChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int returnVal = imageChooser.showOpenDialog(this);
        if(returnVal == JFileChooser.APPROVE_OPTION){
            File F = imageChooser.getSelectedFile();
            tempImgPath = F.getName();
            figImg.setIcon(new ImageIcon(scaleImg(F, 120,180)));
        }
        
    }//GEN-LAST:event_imgBtnActionPerformed

