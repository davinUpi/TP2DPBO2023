    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        modelFigure F = new modelFigure();
        modelManuf M = new modelManuf();

        if(figure != null){
            figure.setName(nameField.getText());
            figure.setType(typeBox.getModel().getSelectedItem().toString());
            figure.setImg(tempImgPath);
            figure.setManufacturer(M.getManufacturer(manBox.getModel().getSelectedItem().toString()));
            importImage(figure.getImg());
            int check = F.updateFigure(figure);
            if(check > 0){
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Gagal update");
            }
        }
        else{
            importImage(tempImgPath);
            int check = F.insertFigure(
                    nameField.getText(), 
                    typeBox.getModel().getSelectedItem().toString(),
                    tempImgPath,
                    M.getManufacturer(manBox.getModel().getSelectedItem().toString())
                    );
            if(check > 0){
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Gagal add");
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

