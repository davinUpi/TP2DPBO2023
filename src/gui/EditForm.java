    private void setInitialVal(){
        nameField.setText(figure.getName());
        typeBox.getModel().setSelectedItem(figure.getType().getStringVal());
        manBox.getModel().setSelectedItem(figure.getManufacturer().getName());
        tempImgPath = figure.getImg();
        figImg.setIcon(new ImageIcon(scaleImg(tempImgPath,120,180)));
    }
    
    private ComboBoxModel setTypeBox(){
        String [] val = new String[FigureType.values().length];
        int counter = 0;
        for(FigureType i : FigureType.values()){
            val[counter] = i.getStringVal();
            counter++;
        }
        return new DefaultComboBoxModel(val);
    }
    
    private ComboBoxModel setManBox(){
        modelManuf m = new modelManuf();
        ArrayList<Manuf> listM = m.getManufacturers();
        String [] val = new String[listM.size()];
        int counter = 0 ;
        for(Manuf i : listM){
            val[counter] = i.getName();
            counter++;
        }
        return new DefaultComboBoxModel(val);
    }
    
    private Image scaleImg(String img_path, int width, int height){
        try{
            imgFile = ImageIO.read(getClass().getResource("/img/"+img_path));
            Image dimg = imgFile.getScaledInstance(width, height, Image.SCALE_SMOOTH);
            return dimg;
        }catch (IOException e){
            e.printStackTrace();
            return null;
        }
    }
    
    private Image scaleImg(File img_file, int width, int height){
        try{
            imgFile = ImageIO.read(img_file);
            Image dimg = imgFile.getScaledInstance(width, height, Image.SCALE_SMOOTH);
            return dimg;
        }catch (IOException e){
            e.printStackTrace();
            return null;
        }
    }
    
    private void importImage(String img_path){
        try{
            String path = "build/classes/img/"+img_path;
            File F = new File(path);
            if(F.createNewFile()){
                ImageIO.write(imgFile, img_path.substring(img_path.lastIndexOf(".") + 1), F);
            }
            path = "src/img/"+img_path;
            F = new File(path);
            if(F.createNewFile()){
                ImageIO.write(imgFile, img_path.substring(img_path.lastIndexOf(".") + 1), F);
            }
        } catch (IOException e){
            e.printStackTrace();
        }
    }

